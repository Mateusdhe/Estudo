# Condicional Composta
#  Se ambas as variáveis booleanas cima e baixo forem True,
#  escreva: “Decida-se!”, caso contrário, escreva:
#  “Você escolheu um caminho!”
cima  = True
baixo = True

if (cima == True and baixo == True):
    print('Decida-se!')  
else:
    print('Você escolheu um caminho!')    